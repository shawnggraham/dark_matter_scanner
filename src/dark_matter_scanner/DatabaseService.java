    // ========================================
    // BLOCK 15 — LOAD SCAN NODE ADDRESSES
    // ========================================
    public List<NodeAddress> loadScanNodeAddresses(int scanId) throws Exception {

        String sql = """
            WITH ranked AS (
                SELECT
                    host(observed_ip) AS observed_ip,
                    mac_address,
                    COALESCE(v.vendor_clean, v.vendor, 'Unknown') AS vendor,
                    ROW_NUMBER() OVER (
                        PARTITION BY observed_ip
                        ORDER BY
                            (CASE WHEN rtt_ms IS NOT NULL THEN 1 ELSE 0 END) DESC,
                            (CASE WHEN mac_address IS NOT NULL THEN 1 ELSE 0 END) DESC,
                            seen_count DESC,
                            last_seen DESC NULLS LAST
                    ) AS rn
                FROM arp_bindings
                LEFT JOIN mac_oui_lookup v
                  ON LEFT(REGEXP_REPLACE(UPPER(arp_bindings.mac_address), '[^0-9A-F]', '', 'g'), 6)
                   = LEFT(REGEXP_REPLACE(UPPER(v.oui),                   '[^0-9A-F]', '', 'g'), 6)
                WHERE scan_id = ?
                  AND observed_ip IS NOT NULL
            )
            SELECT observed_ip, mac_address, vendor
            FROM ranked
            WHERE rn = 1
            ORDER BY observed_ip
        """;

        List<NodeAddress> rows = new ArrayList<>();

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            stmt.setInt(1, scanId);

            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {
                    rows.add(new NodeAddress(
                            rs.getString("observed_ip"),
                            rs.getString("mac_address"),
                            rs.getString("vendor")
                    ));
                }
            }
        }

        return rows;
    }

