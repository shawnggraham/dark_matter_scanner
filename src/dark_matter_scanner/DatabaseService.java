    // ========================================
    // BLOCK 11B — BATCH INSERT MAC OBSERVATIONS
    // ========================================
    public int insertMacObservationsBatch(List<TcpdumpParserService.MacObservationRecord> rows) throws Exception {

        if (rows == null || rows.isEmpty()) {
            return 0;
        }

        String sql = """
            INSERT INTO mac_observations
                (scan_id, capture_id, src_mac, src_ip, dst_mac, dst_ip, protocol, note, raw_line)
            VALUES
                (?, ?, ?, ?::inet, ?, ?::inet, ?, ?, ?)
        """;

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            conn.setAutoCommit(false);

            try {
                int batchCount = 0;

                for (TcpdumpParserService.MacObservationRecord row : rows) {
                    stmt.setInt(1, row.getScanId());
                    stmt.setLong(2, row.getCaptureId());

                    if (row.getSrcMac() != null && !row.getSrcMac().isBlank()) {
                        stmt.setString(3, row.getSrcMac());
                    } else {
                        stmt.setNull(3, Types.VARCHAR);
                    }

                    if (row.getSrcIp() != null && !row.getSrcIp().isBlank()) {
                        stmt.setString(4, row.getSrcIp());
                    } else {
                        stmt.setNull(4, Types.VARCHAR);
                    }

                    if (row.getDstMac() != null && !row.getDstMac().isBlank()) {
                        stmt.setString(5, row.getDstMac());
                    } else {
                        stmt.setNull(5, Types.VARCHAR);
                    }

                    if (row.getDstIp() != null && !row.getDstIp().isBlank()) {
                        stmt.setString(6, row.getDstIp());
                    } else {
                        stmt.setNull(6, Types.VARCHAR);
                    }

                    stmt.setString(7, row.getProtocol() != null ? row.getProtocol() : "ARP");

                    if (row.getNote() != null && !row.getNote().isBlank()) {
                        stmt.setString(8, row.getNote());
                    } else {
                        stmt.setNull(8, Types.VARCHAR);
                    }

                    stmt.setString(9, row.getRawLine());
                    stmt.addBatch();
                    batchCount++;

                    if (batchCount % 1000 == 0) {
                        stmt.executeBatch();
                    }
                }

                stmt.executeBatch();
                conn.commit();
                return rows.size();

            } catch (Exception e) {
                conn.rollback();
                throw e;
            } finally {
                conn.setAutoCommit(true);
            }
        }
    }

    // ========================================
    // BLOCK 11C — UPSERT PASSIVE ARP BINDINGS FROM OBSERVATIONS
    // ========================================
    public int upsertPassiveArpBindingsFromObservations(int scanId, List<TcpdumpParserService.MacObservationRecord> rows) throws Exception {

        if (rows == null || rows.isEmpty()) {
            return 0;
        }

        String sql = """
            INSERT INTO arp_bindings (scan_id, observed_ip, mac_address, source, rtt_ms, response_type)
            VALUES (?, ?::inet, ?, 'passive_tcpdump', NULL, 'passive_tcpdump')
            ON CONFLICT (scan_id, observed_ip, mac_address, source)
            DO UPDATE SET
                last_seen = now(),
                seen_count = arp_bindings.seen_count + 1
        """;

        String scanSubnet = getScanSubnet(scanId);

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            conn.setAutoCommit(false);

            try {
                int promotedCount = 0;
                Set<String> seenPairs = new HashSet<>();

                for (TcpdumpParserService.MacObservationRecord row : rows) {
                    if (!"ARP".equalsIgnoreCase(row.getProtocol())) {
                        continue;
                    }
                    promotedCount += addPassiveCandidate(stmt, seenPairs, scanId, scanSubnet, row.getSrcIp(), row.getSrcMac());
                    promotedCount += addPassiveCandidate(stmt, seenPairs, scanId, scanSubnet, row.getDstIp(), row.getDstMac());
                }

                stmt.executeBatch();
                conn.commit();
                return promotedCount;

            } catch (Exception e) {
                conn.rollback();
                throw e;
            } finally {
                conn.setAutoCommit(true);
            }
        }
    }

    // ========================================
    // BLOCK 11D — UPSERT PASSIVE IPV4 UNICAST BINDINGS FROM OBSERVATIONS
    // ========================================
    public int upsertPassiveIpv4BindingsFromObservations(int scanId, List<TcpdumpParserService.MacObservationRecord> rows) throws Exception {

        if (rows == null || rows.isEmpty()) {
            return 0;
        }

        String sql = """
            INSERT INTO arp_bindings (scan_id, observed_ip, mac_address, source, rtt_ms, response_type)
            VALUES (?, ?::inet, ?, 'passive_ipv4', NULL, 'passive_ipv4')
            ON CONFLICT (scan_id, observed_ip, mac_address, source)
            DO UPDATE SET
                last_seen = now(),
                seen_count = arp_bindings.seen_count + 1
        """;

        String scanSubnet = getScanSubnet(scanId);

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            conn.setAutoCommit(false);

            try {
                int promotedCount = 0;
                Set<String> seenPairs = new HashSet<>();

                for (TcpdumpParserService.MacObservationRecord row : rows) {
                    if (!"IPv4".equalsIgnoreCase(row.getProtocol())) {
                        continue;
                    }
                    if (row.getNote() != null && !"unicast".equalsIgnoreCase(row.getNote())) {
                        continue;
                    }
                    promotedCount += addPassiveCandidate(
                            stmt,
                            seenPairs,
                            scanId,
                            scanSubnet,
                            row.getSrcIp(),
                            row.getSrcMac()
                    );
                }

                stmt.executeBatch();
                conn.commit();
                return promotedCount;

            } catch (Exception e) {
                conn.rollback();
                throw e;
            } finally {
                conn.setAutoCommit(true);
            }
        }
    }

    private int addPassiveCandidate(
            PreparedStatement stmt,
            Set<String> seenPairs,
            int scanId,
            String scanSubnet,
            String ip,
            String mac
    ) throws Exception {

        if (!isUsableIpv4(ip) || !isUsableUnicastMac(mac)) {
            return 0;
        }

        String normalizedIp = ip.trim();
        if (!isIpInSubnet(normalizedIp, scanSubnet)) {
            return 0;
        }
        String normalizedMac = mac.trim().toUpperCase();
        String key = normalizedIp + "|" + normalizedMac;

        if (!seenPairs.add(key)) {
            return 0;
        }

        stmt.setInt(1, scanId);
        stmt.setString(2, normalizedIp);
        stmt.setString(3, normalizedMac);
        stmt.addBatch();
        return 1;
    }

