    private boolean isUsableIpv4(String ip) {
        if (ip == null) {
            return false;
        }
        String value = ip.trim();
        if (value.isEmpty() || "0.0.0.0".equals(value)) {
            return false;
        }

        String[] parts = value.split("\\.");
        if (parts.length != 4) {
            return false;
        }

        for (String part : parts) {
            if (part.isEmpty() || part.length() > 3) {
                return false;
            }
            try {
                int octet = Integer.parseInt(part);
                if (octet < 0 || octet > 255) {
                    return false;
                }
            } catch (NumberFormatException e) {
                return false;
            }
        }

        return true;
    }

    private boolean isUsableUnicastMac(String mac) {
        if (mac == null) {
            return false;
        }
        String value = mac.trim().toUpperCase();

        if (!value.matches("^[0-9A-F]{2}(:[0-9A-F]{2}){5}$")) {
            return false;
        }
        if ("00:00:00:00:00:00".equals(value) || "FF:FF:FF:FF:FF:FF".equals(value)) {
            return false;
        }

        try {
            int firstOctet = Integer.parseInt(value.substring(0, 2), 16);
            return (firstOctet & 1) == 0;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    private boolean isIpInSubnet(String ip, String subnetCidr) {
        if (!isUsableIpv4(ip) || subnetCidr == null || subnetCidr.isBlank()) {
            return false;
        }

        String cidr = subnetCidr.trim();
        int slash = cidr.indexOf('/');
        if (slash <= 0 || slash >= cidr.length() - 1) {
            return false;
        }

        String networkIp = cidr.substring(0, slash).trim();
        String prefixText = cidr.substring(slash + 1).trim();
        if (!isUsableIpv4(networkIp)) {
            return false;
        }

        int prefix;
        try {
            prefix = Integer.parseInt(prefixText);
        } catch (NumberFormatException e) {
            return false;
        }
        if (prefix < 0 || prefix > 32) {
            return false;
        }

        long ipValue = ipv4ToLong(ip);
        long netValue = ipv4ToLong(networkIp);
        if (ipValue < 0 || netValue < 0) {
            return false;
        }

        long mask = prefix == 0 ? 0L : (0xFFFFFFFFL << (32 - prefix)) & 0xFFFFFFFFL;
        return (ipValue & mask) == (netValue & mask);
    }

    private long ipv4ToLong(String ip) {
        if (!isUsableIpv4(ip)) {
            return -1L;
        }

        String[] parts = ip.split("\\.");
        long value = 0L;
        for (String part : parts) {
            int octet;
            try {
                octet = Integer.parseInt(part);
            } catch (NumberFormatException e) {
                return -1L;
            }
            value = (value << 8) | (octet & 0xFFL);
        }
        return value;
    }

