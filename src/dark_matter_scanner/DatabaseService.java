    // ========================================
    // BLOCK 23 — NORMALIZE OUI TOKEN
    // ========================================
    private String normalizeOuiToken(String input) {
        if (input == null) {
            return null;
        }

        String hex = input.toUpperCase().replaceAll("[^0-9A-F]", "");
        if (hex.length() < 6) {
            return null;
        }
        hex = hex.substring(0, 6);
        return hex.substring(0, 2) + ":" + hex.substring(2, 4) + ":" + hex.substring(4, 6);
    }

