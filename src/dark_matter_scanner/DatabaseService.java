    // ========================================
    // BLOCK 25 — GET OUI METADATA
    // ========================================
    public OuiMetadataRecord getOuiMetadata(String oui) throws Exception {
        String normalizedOui = normalizeOuiToken(oui);
        if (normalizedOui == null) {
            return null;
        }

        String sql = """
            SELECT
                m.oui,
                COALESCE(v.vendor_clean, v.vendor, 'Unknown') AS vendor,
                m.critical_infrastructure,
                m.equipment_type,
                m.role_hint,
                m.notes
            FROM mac_oui_metadata m
            LEFT JOIN mac_oui_lookup v
              ON LEFT(REGEXP_REPLACE(UPPER(m.oui), '[^0-9A-F]', '', 'g'), 6)
               = LEFT(REGEXP_REPLACE(UPPER(v.oui), '[^0-9A-F]', '', 'g'), 6)
            WHERE m.oui = ?
        """;

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            ensureOuiMetadataTable(conn);
            stmt.setString(1, normalizedOui);

            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    return new OuiMetadataRecord(
                            rs.getString("oui"),
                            rs.getString("vendor"),
                            rs.getBoolean("critical_infrastructure"),
                            rs.getString("equipment_type"),
                            rs.getString("role_hint"),
                            rs.getString("notes")
                    );
                }
            }
        }

        return null;
    }

