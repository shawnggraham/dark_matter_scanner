    // ========================================
    // BLOCK 28 — LOAD TRAFFIC ROLE HINTS
    // ========================================
    public List<TrafficRoleHint> loadTrafficRoleHintsForScan(int scanId) throws Exception {
        String sql = """
            WITH latest_capture AS (
                SELECT id
                FROM tcpdump_captures
                WHERE scan_id = ?
                ORDER BY id DESC
                LIMIT 1
            ),
            node_base AS (
                SELECT
                    host(a.observed_ip) AS observed_ip,
                    MAX(a.mac_address) FILTER (WHERE a.mac_address IS NOT NULL) AS mac_address
                FROM arp_bindings a
                WHERE a.scan_id = ?
                  AND a.observed_ip IS NOT NULL
                GROUP BY host(a.observed_ip)
            ),
            flow AS (
                SELECT
                    host(mo.src_ip) AS src_ip,
                    host(mo.dst_ip) AS dst_ip
                FROM mac_observations mo
                JOIN latest_capture lc
                  ON lc.id = mo.capture_id
                WHERE mo.scan_id = ?
                  AND mo.protocol = 'IPv4'
                  AND mo.src_ip IS NOT NULL
                  AND mo.dst_ip IS NOT NULL
            ),
            out_stats AS (
                SELECT
                    src_ip AS observed_ip,
                    COUNT(*) AS outbound_frames,
                    COUNT(DISTINCT dst_ip) AS distinct_outbound_peers
                FROM flow
                GROUP BY src_ip
            ),
            in_stats AS (
                SELECT
                    dst_ip AS observed_ip,
                    COUNT(*) AS inbound_frames,
                    COUNT(DISTINCT src_ip) AS distinct_inbound_peers
                FROM flow
                GROUP BY dst_ip
            ),
            svc AS (
                SELECT
                    host(observed_ip) AS observed_ip,
                    COUNT(*) FILTER (WHERE state = 'open') AS open_service_count
                FROM service_observations
                WHERE scan_id = ?
                GROUP BY host(observed_ip)
            )
            SELECT
                nb.observed_ip,
                nb.mac_address,
                COALESCE(o.outbound_frames, 0) AS outbound_frames,
                COALESCE(i.inbound_frames, 0) AS inbound_frames,
                COALESCE(o.distinct_outbound_peers, 0) AS distinct_outbound_peers,
                COALESCE(i.distinct_inbound_peers, 0) AS distinct_inbound_peers,
                COALESCE(s.open_service_count, 0) AS open_service_count
            FROM node_base nb
            LEFT JOIN out_stats o ON o.observed_ip = nb.observed_ip
            LEFT JOIN in_stats i ON i.observed_ip = nb.observed_ip
            LEFT JOIN svc s ON s.observed_ip = nb.observed_ip
            ORDER BY nb.observed_ip
        """;

        List<TrafficRoleHint> rows = new ArrayList<>();

        try (
                Connection conn = getConnection();
                PreparedStatement stmt = conn.prepareStatement(sql)
        ) {
            stmt.setInt(1, scanId);
            stmt.setInt(2, scanId);
            stmt.setInt(3, scanId);
            stmt.setInt(4, scanId);

            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {
                    rows.add(new TrafficRoleHint(
                            rs.getString("observed_ip"),
                            rs.getString("mac_address"),
                            rs.getInt("outbound_frames"),
                            rs.getInt("inbound_frames"),
                            rs.getInt("distinct_outbound_peers"),
                            rs.getInt("distinct_inbound_peers"),
                            rs.getInt("open_service_count")
                    ));
                }
            }
        }

        return rows;
    }

